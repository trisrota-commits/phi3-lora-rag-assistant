IGNORE_INDEX = -100

def tokenize(example):
    full_prompt   = tokenizer.apply_chat_template(example["messages"],      tokenize=False)
    prompt_only   = tokenizer.apply_chat_template(example["messages"][:-1], tokenize=False,
                                                   add_generation_prompt=True)

    full_enc   = tokenizer(full_prompt,  truncation=True, max_length=512)
    prompt_enc = tokenizer(prompt_only,  truncation=True, max_length=512)

    input_ids      = full_enc["input_ids"]
    attention_mask = full_enc["attention_mask"]
    prompt_len     = len(prompt_enc["input_ids"])

    # Only compute loss on the assistant (Yoda) tokens
    labels = [IGNORE_INDEX] * prompt_len + input_ids[prompt_len:]

    return {"input_ids": input_ids, "attention_mask": attention_mask, "labels": labels}

