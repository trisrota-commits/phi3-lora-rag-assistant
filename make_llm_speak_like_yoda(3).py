def rag_answer(question):
    # Format context as clean text, NOT as a Python list repr
    context = "\n\n---\n\n".join(retrieve(question))

    messages = [
        {"role": "system", "content": YODA_SYSTEM},
        {
            "role": "user",
            "content": (
                f"Use the context below to answer the question.\n\n"
                f"Context:\n{context}\n\n"
                f"Question: {question}\n\n"
                f"Answer in Yoda's speech style:"
            ),
        },
    ]

    inputs = tokenizer.apply_chat_template(
        messages,
        add_generation_prompt=True,
        return_tensors="pt",
    ).to(model.device)

    with torch.no_grad():
        outputs = model.generate(
            inputs,
            max_new_tokens=200,
            do_sample=True,
            temperature=0.7,
            top_p=0.85,
            repetition_penalty=1.3,
            eos_token_id=tokenizer.eos_token_id,
        )

    # Decode ONLY the new tokens (not the prompt)
    new_tokens = outputs[0][inputs.shape[-1]:]
    return tokenizer.decode(new_tokens, skip_special_tokens=True)

